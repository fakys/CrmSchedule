<?php

namespace App\Modules\Crm\auth\providers;

use App\Modules\Crm\auth\InfoModule;
use App\Src\modules\providers\AbstractModulesProvider;

class AuthModuleProvider extends AbstractModulesProvider
{

    public function prefixModulePathName(): string
    {
        return 'Crm';
    }

    public function modulePathName(): string
    {
        return 'auth';
    }

    /**
     * Register services.
     */
    public function register(): void
    {
        parent::register();

    }

    /**
     * Bootstrap services.
     */
    public function boot(): void
    {
        parent::boot();

    }

    public function moduleModel(): string
    {
        return InfoModule::class;
    }
}
